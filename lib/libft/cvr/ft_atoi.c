#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		less;
	int		nb;

	i = 0;
	less = 1;
	nb = 0;
	while (((str[i] == '\t') || (str[i] == '\n') || (str[i] == '\r')
				|| (str[i] == ' ') || (str[i] == '\v') || (str[i] == '\f'))
			&& (str[i] != '\0'))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			less = -1;
		i++;
	}
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		nb = (nb * 10) + (str[i] - 48);
		i++;
	}
	return (nb * less);
}
